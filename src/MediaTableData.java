
public class MediaTableData {
    MediaTableData(final String in_name,
                   final int    in_year,
                   final String in_rating,
                   final String in_fan) {
        name = in_name;
        year = in_year;
        rating = in_rating;
        fan = in_fan;
    }

    public String name;
    public int    year;
    public String rating;
    public String fan;
}
